/*Tipos de datos

Primitivos
    numericos (int y double)
    Strings 
    Boolean
    Char
Comoplejos
    Objetos {}
    Arreglos []

Fuertemente tipado 
Que el lenguaje de porgramacion se requiere saber explicitamente el tipo de dato al 
momento de crear una variable.
Ejemplo(Java)
int numberVar = 10;
double decimalNumber = 23.56;
String myName = 'Cesar';
char myLetter = 'a';
boolean myCondition = True;

Debilmente tipado
Significa que el lenguaje interpreta el tipo de dato y no se requiere que se
defina explicitamente el tipo de dato.
Ejemplo(Javascript)
let number = 10;
*/